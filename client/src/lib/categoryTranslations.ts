// Helper function to translate category name
export function translateCategory(chineseName: string, targetLang: string): string {
  const translations = categoryTranslations[chineseName];
  if (!translations) {
    return chineseName; // Return original if no translation found
  }
  return translations[targetLang] || chineseName;
}
